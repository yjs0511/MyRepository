
package ch11.exam13;

public class BoxingUnBoxingExample {

	public static void main(String[] args) {
		Integer obj = 100;
		
		int value = obj;	
		System.out.println(value);
		
		int result = obj + 200;	//obj 객체 안의 100을 꺼내 200이랑 더하자! 이렇게 기억해라
		System.out.println(result);
		
		
	}

}
